package abacusAI;

public class Q03 {
    // 3. If-Else Conditional
    // Problem:
    // Write a program that checks if a number is positive or negative. Print "Positive" if the number is greater than 0, otherwise print "Negative".
    //
    // Example Input:
    // -5
    // Example Output:
    // Negative
    //
    // Targeted Concept:
    // Conditional Statements â€“ Using if-else to control program flow.

    public static void main(String[] args) {
        int a = -5;

        if (a > 0) {
            System.out.println("Positive");
        } else {
            System.out.println("Negative");
        } // Correctly close the else block
    } // Correctly close the main method
}
