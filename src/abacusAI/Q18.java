package abacusAI;

public class Q18
{

	public static void main(String[] args)
	{
//		18. Enhanced For Loop
//		Problem:
//		Use an enhanced for loop to print all elements of a string array.
//
//		Example Output:
//
//		apple
//		banana
//		cherry
//		Targeted Concept:
//		Enhanced For Loop â€“ Iterating over arrays with for-each (enhanced for) loop
//
//	
		String[] fruits = { "apple", "banana", "cherry" };
		// Enhanced for loop to iterate over the array
		for (String fruit : fruits)
		{
			System.out.println(fruit);
		}
		
		// int num array for loop
		int[] numbers = { 1, 2, 3, 4, 5 };
		// Enhanced for loop to iterate over the array
		for (int number : numbers)
		{
			System.out.println(number);
		}
		
		
		
} // end of main
} // end of class