package abacusAI2;

public class DataTypesDemo
{

//attributes	
public int number;
public double decimalNumber;
public boolean isTrue;

//constructors
public DataTypesDemo()
{ this.number = 0;
this.decimalNumber = 0.0;
this.isTrue = false;
}


public DataTypesDemo(int number, double decimalNumber, boolean isTrue)
{ this.number = number;
this.decimalNumber = decimalNumber;
this.isTrue = isTrue;
}


}