package chatGPT;

public class Q09
{

	public static void main(String[] args)
	{
//9. Countdown with Steps
//Topic: while loop
//Task: Count down from 100 to 50 in steps of 5.
//Output: 100 95 90 ... 50
//Target Concept: Decremental looping.
		int N=100;
		while (N>=50) {
		System.out.print(N + " ");
		N--;
		}

	}

}
